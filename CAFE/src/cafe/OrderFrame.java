    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField2.getText().isEmpty()){
            JOptionPane.showConfirmDialog(this, "Please select a product","Warning",2);
        }else if(jTextField3.getText().isEmpty()|| jTextField3.getText().equals("0")){
              JOptionPane.showConfirmDialog(this, "Product quantity is required","Warning",2);
        }else{          
            try{
                  model=(DefaultTableModel) jTable1.getModel();
            int cid = Integer.parseInt(jTextField1.getText().trim());
            int qty=Integer.parseInt(jTextField3.getText().trim());
            int proId=Integer.parseInt(model.getValueAt(rowIndex, 0).toString());
            String pName=jTextField2.getText().trim();
            
            price = Double.parseDouble(model.getValueAt(rowIndex, 2).toString());
            
            if(!dao.isProductExist(cid, proId)){
                Cart cart=new Cart();
                cart.setId(cid);
                cart.setPid(proId);
                cart.setpName(pName);
                cart.setQty(qty);
                cart.setPrice(price);
                cart.setTotal(price*(double) qty);
                total+=price*(double) qty;
                
                jLabel1.setText(String.format("Total (VND):"+"%.2f",total));
                if(dao.insertCart(cart)){
                    JOptionPane.showConfirmDialog(this, "Product added");
                    clear();
                }
            }else{
                JOptionPane.showConfirmDialog(this, "This product already exits","Warning",2);
            }
            }catch(Exception e){
                e.printStackTrace();
                JOptionPane.showConfirmDialog(this, ""+e,"Warning",2);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void clear(){
        jTextField2.setText(null);
        jTextField3.setText("0");
        jTable1.clearSelection();
    }
    
